public class DemoASCII {
  public static void main(String[] args) {
    char x = 'a';
    System.out.println(x); // a

    int ascii = x;
    System.out.println(ascii);

    System.out.println('b' - 'a');


    // given a - z, convert to 1-26
    char v = 'a';
    int ans = v - 'a' + 1;
    System.out.println(ans);


    System.out.println('O' - 'a');


  }
}
